// System.out.println 사용
public class cat {

    /*
    - 이스케이프 시퀀스
    1. \\ : \ 출력
    2. \n : 개행
    3. \r : 캐리지 리턴 (해당 줄의 처음(맨 앞)으로 이동됨
    4. \t : 수평 탭
    5. \b : 백 스페이스 (바로 앞의 문자를 하나 지움)
    6. \f : 폼파드 (프린터에서 강제로 다음 페이지로 넘길 때 사용)
    7. \' : ' 출력
    8. \" : " 출력
     */
    public static void main(String[] args) {

        System.out.println("\\    /\\");
        System.out.println(" )  ( ')");
        System.out.println("(  /  )");
        System.out.println(" \\(__)|");
    }
}